#include "ft_printf.h"

char	*ft_str_tolower(char *s)
{
	int	i;

	i = 0;
	if (*s)
	{
		while (s[i] != '\0')
		{
			if (s[i] >= 'A' && s[i] <= 'Z')
				s[i] = (s[i] + 32);
			i++;
		}
	}
	return (s);
}
